package com.valerysamovich.java.exercises;

public class OddEven {

    public static boolean evenNum (double num) {
        
        if (num % 2 == 0) {
            System.out.println(true);
            return true;
        } else {
            System.out.println(false);
            return false;
        }
    }
}
